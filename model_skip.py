class ListModule(nn.Module):
    def __init__(self, *args):
        super(ListModule, self).__init__()
        idx = 0
        for module in args:
            self.add_module(str(idx), module)
            idx += 1

    def __getitem__(self, idx):
        if idx >= len(self._modules):
            raise IndexError('index {} is out of range'.format(idx))
        if idx < 0: 
            idx = len(self) + idx

        it = iter(self._modules.values())
        for i in range(idx):
            next(it)
        return next(it)

    def __iter__(self):
        return iter(self._modules.values())

    def __len__(self):
        return len(self._modules)

def skip_my(
        num_input_channels=2, num_output_channels=3, 
        num_channels_down=[16, 32, 64, 128, 128], num_channels_up=[16, 32, 64, 128, 128], num_channels_skip=[4, 4, 4, 4, 4], 
        filter_size_down=3, filter_size_up=3, filter_skip_size=1,
        need_sigmoid=True, need_bias=True, 
        pad='zero', upsample_mode='nearest', downsample_mode='stride', act_fun='LeakyReLU', 
        need1x1_up=True, drop=False, num_k=3):
    """Assembles encoder-decoder with skip connections.

    Arguments:
        act_fun: Either string 'LeakyReLU|Swish|ELU|none' or module (e.g. nn.ReLU)
        pad (string): zero|reflection (default: 'zero')
        upsample_mode (string): 'nearest|bilinear' (default: 'nearest')
        downsample_mode (string): 'stride|avg|max|lanczos2' (default: 'stride')

    """
    assert len(num_channels_down) == len(num_channels_up) == len(num_channels_skip)

    n_scales = len(num_channels_down)

    if not (isinstance(upsample_mode, list) or isinstance(upsample_mode, tuple)):
        upsample_mode   = [upsample_mode]*n_scales

    if not (isinstance(downsample_mode, list) or isinstance(downsample_mode, tuple)):
        downsample_mode   = [downsample_mode]*n_scales
    
    if not (isinstance(filter_size_down, list) or isinstance(filter_size_down, tuple)):
        filter_size_down   = [filter_size_down]*n_scales

    if not (isinstance(filter_size_up, list) or isinstance(filter_size_up, tuple)):
        filter_size_up   = [filter_size_up]*n_scales

    last_scale = n_scales - 1 

    cur_depth = None

    model = nn.Sequential()
    model_tmp = model

    input_depth = num_input_channels
    for i in range(len(num_channels_down)):

        deeper = nn.Sequential()
        skip = nn.Sequential()

        if num_channels_skip[i] != 0:
            model_tmp.add(Concat(1, skip, deeper))
        else:
            model_tmp.add(deeper)
        
        model_tmp.add(bn(num_channels_skip[i] + (num_channels_up[i + 1] if i < last_scale else num_channels_down[i])))

        if num_channels_skip[i] != 0:
            skip.add(conv(input_depth, num_channels_skip[i], filter_skip_size, bias=need_bias, pad=pad))
            skip.add(bn(num_channels_skip[i]))
            skip.add(act(act_fun))
            
        # skip.add(Concat(2, GenNoise(nums_noise[i]), skip_part))

        deeper.add(conv(input_depth, num_channels_down[i], filter_size_down[i], 2, bias=need_bias, pad=pad, downsample_mode=downsample_mode[i]))
        deeper.add(bn(num_channels_down[i]))
        deeper.add(act(act_fun))
        if i>1:
            deeper.add(NONLocalBlock2D(in_channels=num_channels_down[i]))
            if drop == True:
                deeper.add(dropout(0.5))
        deeper.add(conv(num_channels_down[i], num_channels_down[i], filter_size_down[i], bias=need_bias, pad=pad))
        deeper.add(bn(num_channels_down[i]))
        deeper.add(act(act_fun))

        deeper_main = nn.Sequential()

        if i == len(num_channels_down) - 1:
            # The deepest
            k = num_channels_down[i]
        else:
            deeper.add(deeper_main)
            k = num_channels_up[i + 1]

        deeper.add(nn.Upsample(scale_factor=2, mode=upsample_mode[i]))

        model_tmp.add(conv(num_channels_skip[i] + k, num_channels_up[i], filter_size_up[i], 1, bias=need_bias, pad=pad))
        model_tmp.add(bn(num_channels_up[i]))
        model_tmp.add(act(act_fun))


        if need1x1_up:
            model_tmp.add(conv(num_channels_up[i], num_channels_up[i], 1, bias=need_bias, pad=pad))
            model_tmp.add(bn(num_channels_up[i]))
            model_tmp.add(act(act_fun))

        input_depth = num_channels_down[i]
        model_tmp = deeper_main

    model.add(conv(num_channels_up[0], num_channels_up[0]*(num_k+1), 3, 1, bias=need_bias, pad=pad))
    model.add(conv(num_channels_up[0]*(num_k+1), num_output_channels, 1, bias=need_bias, pad=pad, group=num_k+1))
    if need_sigmoid:
        model.add(nn.Sigmoid())
        
    return model


class CentralMaskedConv2d(nn.Conv2d):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)

        self.register_buffer('mask', self.weight.data.clone())
        _, _, kH, kW = self.weight.size()
        self.mask.fill_(1)
        self.mask[:, :, kH//2, kH//2] = 0

    def forward(self, x):
        self.weight.data *= self.mask
        return super().forward(x)

class unetConv2(nn.Module):
    def __init__(self, in_size, out_size, norm_layer, need_bias, pad, group=1, central_mask=False):
        super(unetConv2, self).__init__()

        # print(pad)
        # self.central_masked_conv = CentralMaskedConv2d(in_size, in_size, kernel_size=2*2-1, stride=1, padding=2-1)
        if central_mask:
            if norm_layer is not None:
                self.conv1= nn.Sequential(CentralMaskedConv2d(in_size, out_size, kernel_size=2*2-1, stride=1, padding=2-1),
                                            norm_layer(out_size),
                                            nn.ReLU(),)
                self.conv2= nn.Sequential(CentralMaskedConv2d(out_size, out_size, kernel_size=2*2-1, stride=1, padding=2-1),
                                        norm_layer(out_size),
                                        nn.ReLU(),)
            else:
                self.conv1= nn.Sequential(CentralMaskedConv2d(in_size, out_size, kernel_size=2*2-1, stride=1, padding=2-1),
                                            nn.ReLU(),)
                self.conv2= nn.Sequential(CentralMaskedConv2d(out_size, out_size, kernel_size=2*2-1, stride=1, padding=2-1),
                                        nn.ReLU(),)
        else:
            if norm_layer is not None:
                self.conv1= nn.Sequential(conv(in_size, out_size, 3, bias=need_bias, pad=pad, group=group),
                                        norm_layer(out_size),
                                        nn.ReLU(),)
                self.conv2= nn.Sequential(conv(out_size, out_size, 3, bias=need_bias, pad=pad, group=group),
                                        norm_layer(out_size),
                                        nn.ReLU(),)
            else:
                self.conv1= nn.Sequential(conv(in_size, out_size, 3, bias=need_bias, pad=pad, group=group),
                                        nn.ReLU(),)
                self.conv2= nn.Sequential(conv(out_size, out_size, 3, bias=need_bias, pad=pad, group=group),
                                        nn.ReLU(),)
    def forward(self, inputs):
        # inputs = self.central_masked_conv(inputs)
        outputs= self.conv1(inputs)
        outputs= self.conv2(outputs)
        return outputs


class unetDown(nn.Module):
    def __init__(self, in_size, out_size, norm_layer, need_bias, pad):
        super(unetDown, self).__init__()
        self.conv= unetConv2(in_size, out_size, norm_layer, need_bias, pad)
        self.down= nn.AvgPool2d(2, 2)

    def forward(self, inputs):
        outputs= self.down(inputs)
        outputs= self.conv(outputs)
        return outputs


class  unetUp(nn.Module):
    def __init__(self, out_size, upsample_mode, need_bias, pad, norm_layer, same_num_filt=False, group=1):
        super(unetUp, self).__init__()

        num_filt = out_size if same_num_filt else out_size * 2
        if upsample_mode == 'deconv':
            self.up= nn.ConvTranspose2d(num_filt, out_size, 2, stride=2, padding=0)
            self.conv= unetConv2(out_size * 2, out_size, norm_layer, need_bias, pad, group=group)
        elif upsample_mode=='bilinear' or upsample_mode=='nearest':
            self.up = nn.Sequential(nn.Upsample(scale_factor=2, mode=upsample_mode),
                                   conv(num_filt, out_size, 3, bias=need_bias, pad=pad))
            self.conv= unetConv2(out_size * 2, out_size, norm_layer, need_bias, pad, group=group)
        else:
            assert False

    def forward(self, inputs1, inputs2):
        in1_up= self.up(inputs1)
        
        if (inputs2.size(2) != in1_up.size(2)) or (inputs2.size(3) != in1_up.size(3)):
            diff2 = (inputs2.size(2) - in1_up.size(2)) // 2 
            diff3 = (inputs2.size(3) - in1_up.size(3)) // 2 
            inputs2_ = inputs2[:, :, diff2 : diff2 + in1_up.size(2), diff3 : diff3 + in1_up.size(3)]
        else:
            inputs2_ = inputs2

        output= self.conv(torch.cat([in1_up, inputs2_], 1))

        return output